// README endpoint reference (injected between markers in each README)
// ════════════════════════════════════════════════════════════════════════════
const escPipe = (s) => String(s).replace(/\|/g, '\\|');

// Idiomatic method signature for a single endpoint, per language.
function refSig(lang, e) {
  const params = parsePath(e.path).params;
  const write = isWrite(e.method);
  const sse = e.kind === 'sse';
  const binary = e.kind === 'binary';
  if (lang === 'node') {
    const ps = params.map(camel);
    if (sse) return `${e.name}Url(${[...ps, 'query?'].join(', ')})`;
    const ret = binary ? ' → BinaryResponse' : '';
    return `${e.name}(${[...ps, write ? 'body?' : 'query?', 'opts?'].join(', ')})${ret}`;
  }
  if (lang === 'python') {
    const ps = params.map(snake);
    if (sse) return `${snake(e.name)}_url(${[...ps, 'query=None'].join(', ')})`;
    return `${snake(e.name)}(${[...ps, write ? 'body=None' : 'query=None', '**opts'].join(', ')})`;
  }
  if (lang === 'ruby') {
    const ps = params.map(snake);
    if (sse) return `${snake(e.name)}_url(${[...ps, 'query = nil'].join(', ')})`;
    return `${snake(e.name)}(${[...ps, write ? 'body = nil' : 'query = nil', 'opts = {}'].join(', ')})`;
  }
  if (lang === 'php') {
    const ps = params.map((p) => '$' + camel(p));
    if (sse) return `${camel(e.name)}Url(${[...ps, '$query = null'].join(', ')})`;
    return `${camel(e.name)}(${[...ps, write ? '$body = null' : '$query = null', 'array $opts = []'].join(', ')})`;
  }
  // go
  const ps = params.map(camel);
  if (sse) return `${pascal(e.name)}URL(${[...ps, 'query...'].join(', ')}) string`;
  if (binary) return `${pascal(e.name)}(ctx, ${[...ps, 'opts...'].join(', ')}) ([]byte, string, error)`;
  return `${pascal(e.name)}(ctx, ${[...ps, 'opts...'].join(', ')}) (json.RawMessage, error)`;
}

const NS_ACCESS = {
  node: (ns) => `client.${camel(ns)}`,
  python: (ns) => `client.${snake(ns)}`,
  ruby: (ns) => `client.${snake(ns)}`,
  php: (ns) => `$client->${camel(ns)}`,
  go: (ns) => `client.${pascal(ns)}`,
};

function buildReference(lang) {
  let count = 0;
  for (const g of manifest.groups) count += g.endpoints.length;
  const L = [];
  L.push(`The SDK exposes **${count} endpoints** across **${manifest.groups.length} namespaces**. Every method is listed below with its idiomatic signature, the underlying HTTP route, and what it does. Path parameters are positional; reads take an optional query map and writes take an optional body, both followed by a call-options bag.`);
  L.push('');
  for (const g of manifest.groups) {
    L.push(`### \`${NS_ACCESS[lang](g.ns)}\` ${g.title}`);
    L.push('');
    L.push(g.desc);
    L.push('');
    L.push('| Method | Endpoint | Description |');
    L.push('| --- | --- | --- |');
    for (const e of g.endpoints) {
      L.push(`| \`${escPipe(refSig(lang, e))}\` | \`${e.method} ${e.path}\` | ${escPipe(e.desc)} |`);
    }
    L.push('');
  }
  return L.join('\n');
}

const BEGIN = '<!-- BEGIN ENDPOINTS (generated by scripts/generate.mjs do not edit) -->';
const END = '<!-- END ENDPOINTS -->';

function injectReference(lang, relPath) {
  const full = join(ROOT, relPath);
  let src;
  try {
    src = readFileSync(full, 'utf8');
  } catch {
    console.warn(`  skip ${relPath} (not found)`);
    return;
  }
  const ref = buildReference(lang);
  const block = `${BEGIN}\n\n${ref}\n${END}`;
  const re = new RegExp(`${BEGIN.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[\\s\\S]*?${END}`);
  let next;
  if (re.test(src)) {
    next = src.replace(re, block);
  } else {
    console.warn(`  ${relPath}: no marker block appending`);
    next = src.replace(/\s*$/, '\n') + '\n' + block + '\n';
  }
  writeFileSync(full, next);
  console.log('  injected reference into', relPath);
}

function genReferences() {
  injectReference('node', 'node/README.md');
  injectReference('python', 'python/README.md');
  injectReference('ruby', 'ruby/README.md');
  injectReference('php', 'php/README.md');
  injectReference('go', 'go/README.md');
}

